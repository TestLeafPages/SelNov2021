package week1.day1;

public class LearnForLoop {

	public static void main(String[] args) {
		//want print 1 to 5
		//for -> used to run same code multiple times
		
		//initialization ; condition ; increment / decrement
		for (int i = 10; i >= 1; i--) {
			System.out.println(i); //2
		}
		

	}

}
